char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;
	int flag;

	flag = 0;
	i = 0;
	while (i < n)
	{
		if (src[i] == '\0')
		{
			flag = 1;
		}
		if (flag != 1)
		{
			*(dest + i) = *(src + i);
		}
		else
		{
			*(dest + i) = '\0';
		}
		i++;
	}
	return (dest);
}

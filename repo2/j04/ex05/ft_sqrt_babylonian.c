#include <stdio.h>

int ft_sqrt(int nb)
{
  float x;
  float y;
  float e;

  x = nb;
  y = 1;
  e = 0.000001;

  while ((x - y) > e) {
    x = (x + y)/2;
    y = nb/x;
  }
  return(x);
}

int main()
{
  int num;
  num = ft_sqrt(16);
  printf("\n111:  %d", num);
  return (0);
}

